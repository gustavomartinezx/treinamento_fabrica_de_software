def some(num1, num2):
    resultado = num1 + num2
    print(resultado)

def multiplique(num1, num2):
    resultado = num1 * num2
    print(resultado)

def divida(num1, num2):
    resultado = num1 / num2
    print(resultado)

a = int(input("Insira o número 1: "))
b = int(input("Insira o número 2: "))
some(a, b)
divida(a, b)
multiplique(a, b)